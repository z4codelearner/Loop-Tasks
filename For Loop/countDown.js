/***

Implement a countdown timer that counts down from 81 to 65.

 */

for(let c = 81; c >= 65; c--){
    console.log(c);
} console.log('Countdown Finished');

/**
 * কিভাবে কাজ করছে (বাংলা ব্যাখ্যা)
for (let i = 81; i >= 65; i--) → লুপ শুরু ৮১ থেকে, প্রতি ধাপে ১ করে কমবে, যতক্ষণ i ৬৫ বা তার উপরে থাকবে।

console.log(i); → প্রতিটি ধাপে বর্তমান সংখ্যা দেখাবে।

লুপ শেষ হলে "Countdown finished!" মেসেজ দেখাবে।
 */