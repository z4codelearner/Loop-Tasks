/***

"I will invest at least 6 hrs every single day for next 60 days!" this message 60 times. So display this.

 */

for (let num = 1; num <= 60; num++){
    console.log("I will invest at least 6 hrs every single day for next 60 days!");
}

/**
 * কিভাবে কাজ করছে (বাংলা ব্যাখ্যা)
for (let i = 1; i <= 60; i++) → এখানে i ১ থেকে শুরু হয়ে ৬০ পর্যন্ত যাবে।

প্রতিবার লুপ চলার সময় console.log(...) দিয়ে মেসেজ প্রিন্ট হবে।

i++ → প্রতি বার i এর মান ১ করে বাড়বে।

যখন i ৬১ হবে, শর্ত i <= 60 মিথ্যা হবে এবং লুপ থামবে।


 */